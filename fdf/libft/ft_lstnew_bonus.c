#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*link_list;

	link_list = (t_list *)malloc(sizeof(t_list));
	if (!(link_list))
		return (NULL);
	link_list->content = content;
	link_list->next = NULL;
	return (link_list);
}
